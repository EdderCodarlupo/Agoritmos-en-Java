    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        DecimalFormat df = new DecimalFormat("#.##");
        double valorVenta = Integer.parseInt(txtValorVenta.getText());
        double igv, precioVenta;
        igv = valorVenta * 0.18;
        precioVenta = valorVenta + igv;
        txtIgv.setText(String.valueOf(df.format(igv)));
        txtPrecioVenta.setText(String.valueOf(df.format(precioVenta)));
    }//GEN-LAST:event_btnCalcularActionPerformed

